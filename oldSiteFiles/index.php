								<!--<div class="mdl-card__media align-card" style="float: right;">										
											<iframe width="20%" height="20%" src="//www.youtube.com/embed/XrKTynrK5nU?rel=0&amp;autoplay=0;vq=hd720" frameborder="0" allowfullscreen></iframe> 

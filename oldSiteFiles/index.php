								<p>
									At Autodesk, I worked in the Strategy and Research department under Pati Vrobel, Distinguished Research Engineer. We tackled a thin vertical 
									into the idea of the computer as a collaborator as it applies to simulation. What I mean by that is in the 
									future, the computer will no longer be acting as a tool. Rather, it will function more as a team member 
									that takes on jobs, runs those processes in the background, gives advice, and is more involved overall 
									in the experience. Think of Jarvis from Iron Man 
								</p>
								<p>
									In the mechanical realm before designs can be manufactured and turned into products, they must first be 
									simulated to assure the design meets the product's requirements. However, the designer is frequently 
									expected to set up the simulation and more often than not, unless they are familiar with that product’s 
									mechanics, they don’t know how to correctly set up the constraints and loads. Even if they get the 
									constraints and loads set correctly most won’t know how to interpret the results. Because of this, many 
									times minor designs are shipped off to the simulation analyst to be professionally tested. The simulation
									analyst will run the simulation, interpret the results, and then send the design back to the designer with 
									critiques. Involving a simulation analyst often creates a bottleneck in process design. Not to mention, 
									for minor designs this is a waste of company resources as the simulation analyst should be applying their 
									knowledge to more complex designs.
								</p>
								<p>
									In collaboration with Patti Vrobel and my team at Autodesk, I designed 
									and implemented a working prototype, Project Prospector, that runs in Autodesk’s Fusion 360 and attempts to
									solve this problem. Prospector automatically sets up and runs simulation in the background while the user
									is designing. Before I give an overview of the project I need to introduce a very powerful utility that 
									made this possible - Design Graph. Design Graph is a project coming out of Autodesk Research that
									uses machine learning and shape recognition to scan designs for their parts and cluster them based off 
									of their geometry. For instance, all of the screws will be in a cluster, the chairs in another, lamps in 
									another, and so on. Users can then explore these clusters when looking for part alternatives by giving 
									Design Graph their design and viewing all similar shaped alternatives.
								</p>
								<p>
									Here is a high level overview of how Prospector works. The user is designing in Fusion 360. Behind the scenes
									Prospector scans their design, queries Design Graph for similar parts and downloads them. Prospector then 
									scans those files for past simulation studies and uses that info to automatically setup and run the simulation 
									on the current design in the background. Imagine a chair being designed in Fusion 360. Prospector takes a
									snapshot of the design, ships it off to Design Graph which returns similar looking chairs. Those similar 
									chairs are then mined for past simulation studies. Let’s say that Prospector came back and found that
									ChairSim1 was the best choice for the user's design. Then Prospector would prepare the user’s chair 
									design for simulation using the info from ChairSim1. Once the simulation is done it is then shown 
									to the designer. All of this is done behind the scene without user interaction. 
								</p>
								<p>
									This project espoused the idea of computer as collaborator in the simulation realm. In the future, 
									if we could filter the simulation’s contours to something that is easier for the designer to understand, 
									we could increase a company’s productivity tremendously by cutting off the simulation analyst bottleneck.
									By shortening the time to manufacture, products get to market faster, increasing company revenues. 
									Prospector allows the designer to design, the simulation analyst to simulate complex designs, and 
									the computer to be used to its full advantage. By the end of the internship we had an awesome working 
									prototype acting as a spark to kindle the innovation behind the computer as a collaborator, opening the 
									eyes of many.
								</p>
